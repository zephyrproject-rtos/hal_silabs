         // Common Mode Buffer Enable
         volatile uint32_t VCMEN: 1;
                  uint32_t reserved0: 1;
         // Accumulation Mode
         volatile uint32_t ACCMD: 1;
         // ADC Tracking Mode
         volatile uint32_t TRKMD: 1;
         // ADC Busy
         volatile uint32_t ADBUSY: 1;
         // Bias Power Select
